
<h2>Política de privacidad</h2>
<p>
    Cuando usa los servicios de nosotros, nos confía su información. Esta
    Política de privacidad está destinada a ayudarlo a entender qué datos
    recopilamos, por qué los recopilamos y qué hacemos con ellos. Esto es
    importante; esperamos que se tome su tiempo para leerlo con cuidado.
    
</p>
<p class="ul">En nuestra Política de Privacidad se explica:</p>
<ul>
    <li>qué información recopilamos y por qué la recopilamos,</li>
    <li>cómo usamos esa información.</li>
</ul>

<h3>Información que recopilamos</h3>
<p>
    Recopilamos información para brindar mejores servicios a todos nuestros
    clientes: cosas básicas como su nombre, apellidos, domicilio, dirección
    de correo electrónico, número de teléfono.
</p>
<p class="ul">Recopilamos información de dos maneras:</p>
<ul>
    <li>
        <strong>Información que usted nos proporciona. </strong>Por ejemplo,
        muchos de nuestros servicios requieren que se registre para obtener
        una cuenta. Cuando lo hace, le solicitamos información personal,
        como su nombre, dirección de correo electrónico, número de teléfono.
    </li>
    <li>
        <strong>
            Información que obtenemos del uso de nuestros servicios. 
        </strong>
        Podemos recopilar información sobre los trabajos que usted solicita
        y los tiempos de entrega.
    </li>
</ul>

<h3>Cómo utilizamos la información que recopilamos</h3>
<p>
    Utilizamos la información que recopilamos de todos nuestros servicios
    para proveerlos, mantenerlos, protegerlos y mejorarlos, para desarrollar
    otros servicios nuevos. También utilizamos esta información para ofrecerle
    contenido personalizado.
</p>
<p>
    Podremos utilizar el nombre que usted proporcione para su perfil en todos
    los servicios que ofrecemos. Además, podremos reemplazar los nombres
    anteriores relacionados con su cuenta de modo que usted sea identificado
    en forma consistente en todos nuestros servicios.
</p>
<p>
    Si usted nos contacta, conservaremos un registro de su comunicación para
    ayudar a solucionar cualquier problema que pueda tener. Podremos utilizar
    su dirección de correo electrónico para informarle sobre nuestros
    servicios, como hacerle saber los próximos cambios o mejoras.
</p>
<p>
    Usamos la información que proporcionan las cookies y otras tecnologías
    para que nuestros usuarios disfruten de una mejor experiencia y para
    mejorar la calidad general de nuestros servicios. Uno de los productos
    que usamos para hacer esto, es Google Analytics.
</p>
